// operadores lógicos (logical operators)

// - 2 valores booleanos, quando verificados, 
// resultará em verdadeiro ou falso

let pao = true
let queijo = true

// AND &&
console.log(pao && queijo); // eu tenho pão e queijo? sim!


// OR ||
console.log(pao || queijo); // tenho pão ou quijo? sim!
// só precisa que uma das variáveis sejam verdadeira para que o resultado seja verdadeiro também


// NOT !
console.log(!pao);  // aqui eu nego o pão, ou seja, se lá em cima ele era true, ficou false
// é tipo o interruptor, se tá ligado, deixo desligado