// incremento ++
let increment = 0
increment++ // some mais um ao zero
console.log(++increment); // existe diferença entre colocar o ++ na frente ou atrás do código 
console.log(increment);


// decremento --
let decrement = 0
console.log(decrement--); //pegue o zero e subtraia um
//obs se colocar depois da variável fica zero, se colocar antes é que fica -1
console.log(--decrement); // nesse caso fica -2 pq o código acima somou -1 e não zero

// exponencial **
console.log(3 ** 2); // 3 elevado a 2